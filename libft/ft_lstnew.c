#include "libft.h"

t_list	*ft_lstnew(int content)
{
	t_list	*beginPointer;

	beginPointer = (t_list *)malloc(sizeof(t_list));
	if (beginPointer == (NULL))
		return (beginPointer);
	beginPointer->value = content;
	beginPointer->next = (NULL);
	return (beginPointer);
}
